#include "libft.h"

char	*ft_strtoupper(char *str)
{
	size_t i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
	return (str);
}
